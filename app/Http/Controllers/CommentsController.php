     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $comments = new Comment();

        $comments->comment = request('comment');
        $comments->category = '';

        $comments->save();

        return redirect('/comments');
    }

